# 3 Задать список из n чисел последовательности (1+ 1/n )**n и вывести на экран их сумму
# пример для n=6 : {1: 4, 2: 7, 3: 10, 4: 13, 5: 16, 6: 19}

# (1+1/n)**n

num = int(input('Укажите число:'))
s = 0
d={}
summ = 0
for i in range(1, num+1):
    s = (1+1/i)**i
    d[i] = s
    summ += s
    print(s)
print(d)
print(summ)
